#[macro_export]
macro_rules! append_fields {
    ($acc:ident, $val:ident, $($field:ident,)*) => (
        $(
            if $val.$field {
                $acc.push(stringify!($field).to_string());
            }
        )*
    );
}

#[macro_export]
macro_rules! append_property {
    ($acc:ident, $val:ident, $($field:ident,)*) => (
        $(
            match format!("{:?}", $val.$field).as_str() {
                "None" => (),
                val => $acc.push(format!("{}: {:?}", stringify!($field), val)),
            };
        )*
    );
}

